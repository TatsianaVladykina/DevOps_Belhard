#!/bin/bash

# Начинаем с нулевой суммы
sum=0

# Читаем числа построчно из стандартного ввода
while read -p "Введите число (для завершения введите 0): " number; do
    # Если введено 0, заканчиваем цикл
    if [ "$number" -eq 0 ]; then
        break
    fi

    # Суммируем введённое число
    sum=$((sum + number))
done

# Выводим результат
echo "Сумма всех введённых чисел: $sum"